def w6(seed, r, s):
    """Parameterized linear cipher (matches AlfaOBD ad::w6).

    Each per-ECU wrapper is just this function with specific (r, s) constants.
    """
    s0, s1, s2, s3 = seed[0], seed[1], seed[2], seed[3]
    v0 = ((s0 << 24) | (s1 << 16) | (s2 << 8) | s3) & 0xFFFFFFFF
    v1 = ((s1 << 24) | (s0 << 16) | (s3 << 8) | s2) & 0xFFFFFFFF
    v1 = (((v1 << 11) & 0xFFFFFFFF) + (v1 >> 22)) & 0xFFFFFFFF
    v2 = (s & v0) & 0xFFFFFFFF
    v1 = (v1 ^ r ^ v2) & 0xFFFFFFFF
    return bytes([(v1 >> 24) & 0xFF, (v1 >> 16) & 0xFF, (v1 >> 8) & 0xFF, v1 & 0xFF])


def w6_by_name(seed, name):
    """Compute w6 key using a pre-catalogued wrapper's parameters."""
    if name not in AOBD_W6_TABLE:
        raise ValueError(f"Unknown w6 wrapper: {name!r}")
    r, s = AOBD_W6_TABLE[name]
    return w6(seed, r, s)


