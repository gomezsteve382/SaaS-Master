#!/usr/bin/env node
/**
 * audit-dispatch-gap.mjs — Task #829
 *
 * Walks the existing routine catalog + UDS frame catalog + dispatch
 * resolution and emits a gap report covering:
 *   - aggregate routines_total / with_frame / orphan / coverage%
 *   - per-ECU breakdown sorted by orphan count desc
 *   - per-orphan heuristic candidate frames (RoutineControl-only,
 *     biased by IL-method co-occurrence on the same ECU family)
 *
 * Pure analysis pass — does NOT touch the upstream extractors or invent
 * new routines/frames. Heuristic candidates are flagged "heuristic-
 * candidate" and are never claimed as confirmed matches.
 *
 * Output: artifacts/srt-lab/src/lib/dispatchGapReport.generated.js
 *
 * Run: pnpm -F @workspace/scripts run audit:dispatch-gap
 */

import { writeFileSync } from "fs";
import { resolve } from "path";

import { ROUTINE_CATALOG_FROM_EXE } from "../../artifacts/srt-lab/src/lib/routineCatalogFromExe.generated.js";
import { UDS_DISPATCH_FRAMES } from "../../artifacts/srt-lab/src/lib/udsDispatchFromExe.generated.js";
import { UDS_FRAME_TO_ROUTINES, MATCHED_DISPATCH_FULL, DISPATCH_TO_ROUTINE_META } from "../../artifacts/srt-lab/src/lib/dispatchToRoutine.generated.js";
import { auditDispatchGap } from "../../artifacts/srt-lab/src/lib/dispatchGapAudit.js";

// Merge unambiguous (UDS_FRAME_TO_ROUTINES) with the broader MATCHED_DISPATCH_FULL
// pool so multi-routine ambiguous matches still count as "has at least one
// candidate frame" rather than orphan. Both are static-IL derived — neither is
// bench-verified.
const merged = { ...UDS_FRAME_TO_ROUTINES };
for (const rec of MATCHED_DISPATCH_FULL) {
  const hex = rec.frame_hex;
  const rids = (rec.matched_routines || []).map((m) => m.rid);
  if (!rids.length) continue;
  const existing = new Set(merged[hex] || []);
  for (const r of rids) existing.add(r);
  merged[hex] = [...existing].sort((a, b) => Number(a) - Number(b));
}

const ROOT = resolve(new URL("../..", import.meta.url).pathname);
const OUT  = resolve(ROOT, "artifacts/srt-lab/src/lib/dispatchGapReport.generated.js");

const report = auditDispatchGap({
  routines: ROUTINE_CATALOG_FROM_EXE,
  frames: UDS_DISPATCH_FRAMES,
  frameToRoutines: merged,
  candidateLimit: 5,
});

// Stamp meta — generatedAt is intentionally a fixed extractor-source
// reference rather than `new Date()` so the committed file stays
// deterministic. The upstream IL extraction date is what matters.
report.meta.generatedAt = "derived from alfaobd-package-2026-05-25 IL extraction";
report.meta.upstreamDispatchMatchedCount = DISPATCH_TO_ROUTINE_META.matched_dispatch_count;
report.meta.upstreamDispatchUnambiguous = DISPATCH_TO_ROUTINE_META.unambiguous_matches;

const banner = `// AUTO-GENERATED by scripts/src/audit-dispatch-gap.mjs — DO NOT EDIT BY HAND.
//
// Re-run: pnpm -F @workspace/scripts run audit:dispatch-gap
//
// Walks routineCatalogFromExe + udsDispatchFromExe + dispatchToRoutine
// and reports the routine→frame coverage gap. Heuristic candidates are
// NEVER confirmed-match — they are starting points for the next bench-
// capture verification pass.
`;

const body = `export const DISPATCH_GAP_REPORT = ${JSON.stringify(report, null, 2)};
`;

writeFileSync(OUT, banner + "\n" + body, "utf8");

const { aggregate, perEcu } = report;
console.log(`Wrote ${OUT}`);
console.log(`  routines_total      = ${aggregate.routinesTotal}`);
console.log(`  routines_with_frame = ${aggregate.routinesWithFrame}`);
console.log(`  routines_orphan     = ${aggregate.routinesOrphan}`);
console.log(`  coverage            = ${aggregate.coveragePercent}%`);
console.log(`  frames_total        = ${aggregate.framesTotal}`);
console.log(`  frames_unattributed = ${aggregate.framesUnattributed}`);
console.log(`  per_ecu_buckets     = ${perEcu.length}`);
console.log(`  worst_ecu           = ${perEcu[0]?.ecu} (${perEcu[0]?.routinesOrphan} orphans)`);
