// Task #643 — format a verifiedAt ISO string for the tooltip / panel.
// Falls back to the raw string if parsing fails so the UI never blanks.
function fmtVerifiedAt(iso) {
  if (!iso) return "";
  try {
    const d = new Date(iso);
    if (Number.isNaN(d.getTime())) return iso;
    return d.toLocaleString();
  } catch {
    return String(iso);
  }
}

// Task #643 — single-line provenance summary suitable for a tooltip /
// title attribute. Mirrors what the panel renders so the badge hover
// matches the expanded card.
function verificationTooltip(meta) {
  if (!meta) return "";
  const parts = [];
  if (meta.operator) parts.push(`by ${meta.operator}`);
  if (meta.verifiedAt) parts.push(`on ${fmtVerifiedAt(meta.verifiedAt)}`);
  if (meta.vin) parts.push(`VIN ${meta.vin}`);
  if (meta.notes) parts.push(`notes: ${meta.notes}`);
  return parts.length === 0
    ? "Bench-verified (no operator metadata recorded)"
    : `Verified ${parts.join(" · ")}`;
}

/**
 * Unlock Coverage tab — Task #499.
 *
 * Renders the contents of /unlock_catalog.json (generated by
 * tools/python-bridge/tools/srtlab_unlock_catalog_gen.py and copied at
 * build time by scripts/copy-unlock-catalog.mjs).
 *
 * For every one of the 81 FCA seed→key DLLs we show:
 *   • file name + module key + display name + DLL size
 *   • decoded ecu_info (CAN tx/rx, ECU name, raw 12-byte hex)
 *   • status badge — "REVERSED" (Python port available) or "DLL ONLY"
 *     (must be emulated under Unicorn — reason text included)
 *   • Python function name when reversed
 *   • family tag for grouping
 *
 * Includes a search box, status filter, family filter, and a small
 * coverage banner with the rolled-up reversed/dll_only counts.
 */
function fmtCanId(n) {
  if (n === null || n === undefined) return "—";
  return "0x" + n.toString(16).toUpperCase().padStart(3, "0");
}

function fmtBytes(n) {
  if (!n) return "—";
  if (n < 1024) return `${n} B`;
  return `${(n / 1024).toFixed(1)} KB`;
}

