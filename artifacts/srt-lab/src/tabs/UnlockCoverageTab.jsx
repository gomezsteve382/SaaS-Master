function VerificationsLogCard({
  verifications, task634Entries,
  source = "pending", syncedAt = null, error = null,
  refreshing = false, onRefresh,
  pendingCount = 0, conflicts = [], onDismissConflict, onAcceptConflict,
  onReviewConflict, onReviewAllConflicts,
}) {
  // Task #661 — persist the verifications-log filters across reloads
  // and tab switches. A shop owner doing daily billing or weekly
  // compliance reviews keeps re-picking the same range; remembering
  // it removes that papercut. Stored as one JSON blob under a single
  // srtlab.* key so an unknown / partial payload simply falls back
  // to the defaults instead of crashing the tab.
  const FILTERS_KEY = "srtlab.task661.verificationsLogFilters.v1";
  function readStoredFilters() {
    if (typeof window === "undefined") return null;
    try {
      const raw = window.localStorage.getItem(FILTERS_KEY);
      if (!raw) return null;
      const v = JSON.parse(raw);
      if (!v || typeof v !== "object") return null;
      return v;
    } catch { return null; }
  }
  const stored = readStoredFilters();
  const [operatorFilter, setOperatorFilter] = useState(
    typeof stored?.operatorFilter === "string" ? stored.operatorFilter : "all");
  const [vinFilter, setVinFilter] = useState(
    typeof stored?.vinFilter === "string" ? stored.vinFilter : "all");
  const [notesQ, setNotesQ] = useState(
    typeof stored?.notesQ === "string" ? stored.notesQ : "");
  // Date range filter — inclusive on both ends. Inputs are HTML date
  // strings (YYYY-MM-DD) interpreted in the user's local timezone so
  // "May 18" means the operator's local May 18, not UTC.
  const [fromDate, setFromDate] = useState(
    typeof stored?.fromDate === "string" ? stored.fromDate : "");
  const [toDate, setToDate] = useState(
    typeof stored?.toDate === "string" ? stored.toDate : "");

  // Mirror the filter state to localStorage whenever any field
  // changes. CLEAR drops every input to its empty default, so the
  // stored payload is correspondingly reset — there is no separate
  // "wipe" branch needed.
  useEffect(() => {
    if (typeof window === "undefined") return;
    try {
      window.localStorage.setItem(FILTERS_KEY, JSON.stringify({
        operatorFilter, vinFilter, notesQ, fromDate, toDate,
      }));
    } catch { /* localStorage may be unavailable / full */ }
  }, [operatorFilter, vinFilter, notesQ, fromDate, toDate]);

  // Convert a local YYYY-MM-DD string into a numeric ms timestamp at the
  // start (00:00:00.000) or end (23:59:59.999) of that local day. Returns
  // null for an empty / malformed string so callers can short-circuit.
  function localDayBounds(ymd, edge) {
    if (!ymd) return null;
    const m = /^(\d{4})-(\d{2})-(\d{2})$/.exec(ymd);
    if (!m) return null;
    const y = Number(m[1]); const mo = Number(m[2]) - 1; const d = Number(m[3]);
    const dt = edge === "end"
      ? new Date(y, mo, d, 23, 59, 59, 999)
      : new Date(y, mo, d, 0, 0, 0, 0);
    const t = dt.getTime();
    return Number.isFinite(t) ? t : null;
  }

  function fmtLocalYmd(date) {
    const y = date.getFullYear();
    const mo = String(date.getMonth() + 1).padStart(2, "0");
    const d = String(date.getDate()).padStart(2, "0");
    return `${y}-${mo}-${d}`;
  }
  function applyPresetToday() {
    const today = fmtLocalYmd(new Date());
    setFromDate(today); setToDate(today);
  }
  function applyPresetThisWeek() {
    // Week = Monday → Sunday in the operator's local timezone. JS getDay
    // returns 0 (Sun)…6 (Sat), so shift so Monday is index 0.
    const now = new Date();
    const dow = (now.getDay() + 6) % 7;
    const monday = new Date(now.getFullYear(), now.getMonth(), now.getDate() - dow);
    const sunday = new Date(monday.getFullYear(), monday.getMonth(), monday.getDate() + 6);
    setFromDate(fmtLocalYmd(monday)); setToDate(fmtLocalYmd(sunday));
  }
  function applyPresetThisMonth() {
    const now = new Date();
    const first = new Date(now.getFullYear(), now.getMonth(), 1);
    const last = new Date(now.getFullYear(), now.getMonth() + 1, 0);
    setFromDate(fmtLocalYmd(first)); setToDate(fmtLocalYmd(last));
  }

  // Index task-634 entries by id so we can resolve a friendly capability
  // label for each verification row. Falls back to the raw entryId when
  // the verification references a row that's been removed from the
  // hand-curated catalog (defensive — should not happen in practice).
  const labelById = useMemo(() => {
    const m = new Map();
    for (const e of task634Entries || []) {
      if (e && typeof e.id === "string") m.set(e.id, e.label || e.id);
    }
    return m;
  }, [task634Entries]);

  // Flatten the verifications dict into an array of rows sorted by
  // verifiedAt desc. Rows missing a verifiedAt sort last so they don't
  // jump to the top with epoch-0 NaN comparisons.
  const rows = useMemo(() => {
    const out = [];
    for (const [entryId, meta] of Object.entries(verifications || {})) {
      if (!meta) continue;
      out.push({
        entryId,
        capability: labelById.get(entryId) || entryId,
        operator: meta.operator || "",
        vin: meta.vin || "",
        notes: meta.notes || "",
        verifiedAt: meta.verifiedAt || "",
      });
    }
    out.sort((a, b) => {
      const ta = a.verifiedAt ? Date.parse(a.verifiedAt) : -Infinity;
      const tb = b.verifiedAt ? Date.parse(b.verifiedAt) : -Infinity;
      return tb - ta;
    });
    return out;
  }, [verifications, labelById]);

  // Distinct operator + VIN values for the filter dropdowns. Sorted
  // case-insensitively so the dropdown reads alphabetically regardless
  // of how the tech typed their initials.
  const operators = useMemo(() => {
    const s = new Set();
    for (const r of rows) if (r.operator) s.add(r.operator);
    return Array.from(s).sort((a, b) => a.localeCompare(b, undefined, {sensitivity: "base"}));
  }, [rows]);
  const vins = useMemo(() => {
    const s = new Set();
    for (const r of rows) if (r.vin) s.add(r.vin);
    return Array.from(s).sort();
  }, [rows]);

  const fromTs = useMemo(() => localDayBounds(fromDate, "start"), [fromDate]);
  const toTs = useMemo(() => localDayBounds(toDate, "end"), [toDate]);

  const filtered = useMemo(() => {
    const term = notesQ.trim().toLowerCase();
    return rows.filter((r) => {
      if (operatorFilter !== "all" && r.operator !== operatorFilter) return false;
      if (vinFilter !== "all" && r.vin !== vinFilter) return false;
      if (term && !r.notes.toLowerCase().includes(term)) return false;
      if (fromTs !== null || toTs !== null) {
        // Rows missing a timestamp can't satisfy a date filter — exclude
        // them so the count + CSV stay consistent with what's visible.
        const t = r.verifiedAt ? Date.parse(r.verifiedAt) : NaN;
        if (!Number.isFinite(t)) return false;
        if (fromTs !== null && t < fromTs) return false;
        if (toTs !== null && t > toTs) return false;
      }
      return true;
    });
  }, [rows, operatorFilter, vinFilter, notesQ, fromTs, toTs]);

  const hasActiveFilter =
    operatorFilter !== "all" || vinFilter !== "all" || notesQ || fromDate || toDate;

  // Earliest / latest verifiedAt across the full (unfiltered) dataset, so
  // the empty-state hint can tell the operator where the data actually
  // lives when their date range excludes everything. Rows without a
  // parseable verifiedAt are ignored.
  const verifiedBounds = useMemo(() => {
    let min = null; let max = null;
    for (const r of rows) {
      if (!r.verifiedAt) continue;
      const t = Date.parse(r.verifiedAt);
      if (!Number.isFinite(t)) continue;
      if (min === null || t < min) min = t;
      if (max === null || t > max) max = t;
    }
    const fmt = (ms) => fmtLocalYmd(new Date(ms));
    return {
      earliest: min !== null ? fmt(min) : null,
      latest: max !== null ? fmt(max) : null,
    };
  }, [rows]);

  function exportCsv() {
    // RFC 4180-ish CSV: wrap every field in double quotes, escape embedded
    // quotes by doubling them. Keeps newlines inside notes intact since the
    // surrounding quotes make that legal.
    const esc = (v) => `"${String(v ?? "").replace(/"/g, '""')}"`;
    const header = ["verifiedAt", "operator", "vin", "capability", "entryId", "notes"];
    const lines = [header.map(esc).join(",")];
    for (const r of filtered) {
      lines.push([
        r.verifiedAt, r.operator, r.vin, r.capability, r.entryId, r.notes,
      ].map(esc).join(","));
    }
    const csv = lines.join("\r\n");
    if (typeof window === "undefined" || typeof document === "undefined") return;
    try {
      const blob = new Blob([csv], {type: "text/csv;charset=utf-8"});
      const url = URL.createObjectURL(blob);
      const stamp = new Date().toISOString().replace(/[:.]/g, "-");
      const a = document.createElement("a");
      a.href = url;
      a.download = `bench-verifications-${stamp}.csv`;
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      // Defer revoke so Safari finishes the download dialog before we
      // pull the rug out from under the blob URL.
      setTimeout(() => URL.revokeObjectURL(url), 1000);
    } catch {
      /* download is best-effort */
    }
  }

  const hasAny = rows.length > 0;
  const inputStyle = {
    fontFamily: "'Nunito'", fontSize: 11,
    padding: "5px 8px", border: `1px solid ${C.bd}`, borderRadius: 4,
    background: "#fff", color: C.tx,
  };

  return (
    <Card data-testid="verifications-log-card">
      <div style={{display: "flex", alignItems: "baseline", gap: 10, flexWrap: "wrap", marginBottom: 8}}>
        <div style={{fontFamily: "'Nunito'", fontWeight: 900, fontSize: 16, color: C.tx}}>
          Verifications log
        </div>
        <span
          data-testid="verifications-log-count"
          style={{
            fontFamily: "'JetBrains Mono', monospace", fontSize: 10, fontWeight: 800,
            letterSpacing: 1, padding: "2px 7px", borderRadius: 999,
            background: "#1B5E2014", color: "#1B5E20",
          }}
        >{filtered.length} of {rows.length}</span>
        <div style={{flex: 1}}/>
        <button
          type="button"
          data-testid="verifications-log-export"
          onClick={exportCsv}
          disabled={filtered.length === 0}
          style={{
            cursor: filtered.length === 0 ? "not-allowed" : "pointer",
            border: "1.5px solid #0D47A1",
            background: filtered.length === 0 ? "#0D47A140" : "#0D47A1",
            color: "#fff", fontFamily: "'Nunito'", fontWeight: 800,
            fontSize: 11, letterSpacing: 0.5, padding: "5px 12px",
            borderRadius: 6, opacity: filtered.length === 0 ? 0.7 : 1,
          }}
        >Export CSV</button>
      </div>
      <div style={{fontFamily: "'Nunito'", fontSize: 11, color: C.tm, marginBottom: 10, lineHeight: 1.4}}>
        Chronological audit of every bench verification recorded for the competitor-parity capabilities — operator, VIN, capability, when, free-form notes. Filter or search, then export CSV for compliance / billing.
      </div>

      {/* Task #652 — source-of-truth banner. Tells the operator whether
          the rows on screen are from a live API GET (server is reachable
          and authoritative) or from the local cache (last successful
          sync persisted to localStorage). The cache state offers a
          manual retry so an operator who just plugged back in can pull
          fresh history without reloading the whole tab. */}
      <VerificationsLogSourceBanner
        source={source}
        syncedAt={syncedAt}
        error={error}
        refreshing={refreshing}
        onRefresh={onRefresh}
        pendingCount={pendingCount}
        conflicts={conflicts}
        onDismissConflict={onDismissConflict}
        onAcceptConflict={onAcceptConflict}
        onReviewConflict={onReviewConflict}
        onReviewAllConflicts={onReviewAllConflicts}
      />

      {!hasAny && (
        <div
          data-testid="verifications-log-empty"
          style={{fontFamily: "'Nunito'", fontSize: 12, color: C.tm, padding: "12px 8px", fontStyle: "italic"}}
        >
          No verifications recorded yet. Mark a capability verified in the Competitor-parity additions card above and it will show up here.
        </div>
      )}

      {hasAny && (
        <>
          <div style={{display: "flex", flexWrap: "wrap", gap: 10, marginBottom: 10, alignItems: "center"}}>
            <label style={{display: "flex", alignItems: "center", gap: 6, fontFamily: "'Nunito'", fontSize: 11, color: C.tm}}>
              Operator
              <select
                data-testid="verifications-log-operator"
                value={operatorFilter}
                onChange={(ev) => setOperatorFilter(ev.target.value)}
                style={inputStyle}
              >
                <option value="all">All ({operators.length})</option>
                {operators.map((op) => (
                  <option key={op} value={op}>{op}</option>
                ))}
              </select>
            </label>
            <label style={{display: "flex", alignItems: "center", gap: 6, fontFamily: "'Nunito'", fontSize: 11, color: C.tm}}>
              VIN
              <select
                data-testid="verifications-log-vin"
                value={vinFilter}
                onChange={(ev) => setVinFilter(ev.target.value)}
                style={inputStyle}
              >
                <option value="all">All ({vins.length})</option>
                {vins.map((v) => (
                  <option key={v} value={v}>{v}</option>
                ))}
              </select>
            </label>
            <input
              type="search"
              data-testid="verifications-log-search"
              value={notesQ}
              onChange={(ev) => setNotesQ(ev.target.value)}
              placeholder="Search notes…"
              style={{...inputStyle, flex: 1, minWidth: 180, fontFamily: "'Nunito'"}}
            />
            {hasActiveFilter && (
              <button
                type="button"
                data-testid="verifications-log-clear"
                onClick={() => {
                  setOperatorFilter("all"); setVinFilter("all"); setNotesQ("");
                  setFromDate(""); setToDate("");
                }}
                style={{
                  cursor: "pointer", border: `1px solid ${C.bd}`,
                  background: "transparent", color: C.tm,
                  fontFamily: "'Nunito'", fontWeight: 700, fontSize: 10,
                  letterSpacing: 1, padding: "5px 10px", borderRadius: 6,
                }}
              >CLEAR</button>
            )}
          </div>

          <div style={{display: "flex", flexWrap: "wrap", gap: 10, marginBottom: 10, alignItems: "center"}}>
            <label style={{display: "flex", alignItems: "center", gap: 6, fontFamily: "'Nunito'", fontSize: 11, color: C.tm}}>
              From
              <input
                type="date"
                data-testid="verifications-log-from"
                value={fromDate}
                max={toDate || undefined}
                onChange={(ev) => setFromDate(ev.target.value)}
                style={inputStyle}
              />
            </label>
            <label style={{display: "flex", alignItems: "center", gap: 6, fontFamily: "'Nunito'", fontSize: 11, color: C.tm}}>
              To
              <input
                type="date"
                data-testid="verifications-log-to"
                value={toDate}
                min={fromDate || undefined}
                onChange={(ev) => setToDate(ev.target.value)}
                style={inputStyle}
              />
            </label>
            <div style={{display: "flex", gap: 4, alignItems: "center"}}>
              {[
                {id: "today", label: "Today", apply: applyPresetToday},
                {id: "week", label: "This week", apply: applyPresetThisWeek},
                {id: "month", label: "This month", apply: applyPresetThisMonth},
              ].map((p) => (
                <button
                  key={p.id}
                  type="button"
                  data-testid={`verifications-log-preset-${p.id}`}
                  onClick={p.apply}
                  style={{
                    cursor: "pointer", border: `1px solid ${C.bd}`,
                    background: "transparent", color: C.tx,
                    fontFamily: "'Nunito'", fontWeight: 700, fontSize: 10,
                    letterSpacing: 0.5, padding: "5px 8px", borderRadius: 6,
                  }}
                >{p.label}</button>
              ))}
            </div>
          </div>

          <div style={{overflowX: "auto", border: `1px solid ${C.bd}`, borderRadius: 6}}>
            <table data-testid="verifications-log-table" style={{width: "100%", borderCollapse: "collapse", fontFamily: "'Nunito'", fontSize: 11}}>
              <thead style={{background: "#0000000A"}}>
                <tr>
                  <th style={{textAlign: "left", padding: "6px 10px", fontSize: 10, color: C.tm, letterSpacing: 1}}>WHEN</th>
                  <th style={{textAlign: "left", padding: "6px 10px", fontSize: 10, color: C.tm, letterSpacing: 1}}>OPERATOR</th>
                  <th style={{textAlign: "left", padding: "6px 10px", fontSize: 10, color: C.tm, letterSpacing: 1}}>VIN</th>
                  <th style={{textAlign: "left", padding: "6px 10px", fontSize: 10, color: C.tm, letterSpacing: 1}}>CAPABILITY</th>
                  <th style={{textAlign: "left", padding: "6px 10px", fontSize: 10, color: C.tm, letterSpacing: 1}}>NOTES</th>
                </tr>
              </thead>
              <tbody>
                {filtered.map((r) => (
                  <tr
                    key={r.entryId}
                    data-testid={`verifications-log-row-${r.entryId}`}
                    style={{borderTop: `1px solid ${C.bd}`}}
                  >
                    <td style={{padding: "6px 10px", color: C.tx, whiteSpace: "nowrap", fontFamily: "'JetBrains Mono', monospace", fontSize: 10}}>
                      {fmtVerifiedAt(r.verifiedAt)}
                    </td>
                    <td style={{padding: "6px 10px", color: C.tx, fontWeight: 700}}>
                      {r.operator || <span style={{color: C.tm, fontStyle: "italic", fontWeight: 400}}>—</span>}
                    </td>
                    <td style={{padding: "6px 10px", color: C.tx, fontFamily: "'JetBrains Mono', monospace", fontSize: 10}}>
                      {r.vin || <span style={{color: C.tm, fontStyle: "italic", fontFamily: "'Nunito'"}}>—</span>}
                    </td>
                    <td style={{padding: "6px 10px", color: C.tx}}>
                      {r.capability}
                      <div style={{fontFamily: "'JetBrains Mono', monospace", fontSize: 9, color: C.tm}}>{r.entryId}</div>
                    </td>
                    <td style={{padding: "6px 10px", color: C.tx, whiteSpace: "pre-wrap", lineHeight: 1.4}}>
                      {r.notes || <span style={{color: C.tm, fontStyle: "italic"}}>—</span>}
                    </td>
                  </tr>
                ))}
                {filtered.length === 0 && (
                  <tr>
                    <td
                      colSpan={5}
                      data-testid="verifications-log-no-match"
                      style={{padding: "16px 10px", textAlign: "center", color: C.tm, fontStyle: "italic"}}
                    >
                      {(fromDate || toDate) ? (
                        <>
                          <div data-testid="verifications-log-no-match-range">
                            {(() => {
                              if (fromDate && toDate) {
                                return `No verifications between ${fromDate} and ${toDate}.`;
                              }
                              if (fromDate) {
                                return `No verifications on or after ${fromDate}.`;
                              }
                              return `No verifications on or before ${toDate}.`;
                            })()}
                          </div>
                          {(verifiedBounds.earliest || verifiedBounds.latest) && (
                            <div
                              data-testid="verifications-log-no-match-bounds"
                              style={{marginTop: 4, fontStyle: "normal", fontSize: 10, color: C.tm}}
                            >
                              {verifiedBounds.earliest === verifiedBounds.latest
                                ? `Only verification on file is ${verifiedBounds.earliest}.`
                                : `Earliest on file is ${verifiedBounds.earliest}, latest is ${verifiedBounds.latest}.`}
                            </div>
                          )}
                        </>
                      ) : (
                        "No verifications match the current filter."
                      )}
                    </td>
                  </tr>
                )}
              </tbody>
            </table>
          </div>
        </>
      )}
    </Card>
  );
}

// Compact UDS code-table renderer used inside the asset-sweep appendix.
// The data shape is `{ "0xNN": "(Name, Description)" | "name" }` straight
// out of the python source — we display it as-is since the value strings
// are already operator-readable.
function UdsCodeList({title, source, codes, testId}) {
  const entries = Object.entries(codes || {}).sort(([a], [b]) =>
    parseInt(a, 16) - parseInt(b, 16));
  return (
    <div data-testid={testId}>
      <div style={{fontFamily: "'Nunito'", fontWeight: 800, fontSize: 12, color: C.tx, marginBottom: 4}}>
        {title} <span style={{fontWeight: 600, color: C.tm}}>({entries.length})</span>
      </div>
      <div style={{fontFamily: "'JetBrains Mono', monospace", fontSize: 9, color: C.tm, marginBottom: 6, wordBreak: "break-all"}}>
        from {source}
      </div>
      <div style={{maxHeight: 220, overflow: "auto", border: `1px solid ${C.bd}`, borderRadius: 6, background: "#0000000A"}}>
        <table style={{width: "100%", borderCollapse: "collapse", fontFamily: "'JetBrains Mono', monospace", fontSize: 10}}>
          <tbody>
            {entries.map(([code, value]) => (
              <tr key={code} style={{borderTop: `1px solid ${C.bd}80`}}>
                <td style={{padding: "3px 8px", color: C.a3, fontWeight: 700, whiteSpace: "nowrap"}}>{code}</td>
                <td style={{padding: "3px 8px", color: C.tx}}>{value}</td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>
    </div>
  );
}
