#!/usr/bin/env node
/**
 * generate-quickref-data.mjs
 *
 * Parses public/srt_lab.py and extracts the canonical bench-reference
 * tables (MODULES, BCM_CANDIDATES, BCM_ALGORITHMS) into a generated
 * JS module that the printable Quick Reference PDF imports.
 *
 * Run automatically as part of `pnpm --filter @workspace/srt-lab build`
 * via the `prebuild` hook. Also runnable manually:
 *   node scripts/generate-quickref-data.mjs            # write
 *   node scripts/generate-quickref-data.mjs --check    # CI: verify in sync
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const PY_PATH = resolve(ROOT, "public/srt_lab.py");
const OUT_PATH = resolve(ROOT, "src/lib/quickRefData.generated.js");

function fail(msg) {
  console.error(`generate-quickref-data: ${msg}`);
  process.exit(1);
}

function sliceBlock(src, header) {
  const startMatch = src.match(new RegExp(`^${header}\\s*=\\s*([\\[{])`, "m"));
  if (!startMatch) fail(`could not find ${header} in srt_lab.py`);
  const open = startMatch[1];
  const close = open === "[" ? "]" : "}";
  const startIdx = startMatch.index + startMatch[0].length;
  let depth = 1;
  let i = startIdx;
  while (i < src.length && depth > 0) {
    const ch = src[i];
    if (ch === open) depth++;
    else if (ch === close) depth--;
    if (depth === 0) break;
    i++;
  }
  if (depth !== 0) fail(`unbalanced ${open}${close} for ${header}`);
  return src.slice(startIdx, i);
}

function parseModules(src) {
  const block = sliceBlock(src, "MODULES");
  const re = /'([^']+)'\s*:\s*\(\s*0x([0-9A-Fa-f]+)\s*,\s*0x([0-9A-Fa-f]+)\s*,\s*"([^"]+)"\s*\)/g;
  const out = [];
  let m;
  while ((m = re.exec(block)) !== null) {
    out.push({
      code: m[1],
      tx: parseInt(m[2], 16),
      rx: parseInt(m[3], 16),
      desc: m[4],
    });
  }
  if (out.length === 0) fail("MODULES parsed empty");
  return out;
}

function parseBcmCandidates(src) {
  const block = sliceBlock(src, "BCM_CANDIDATES");
  const re = /\(\s*0x([0-9A-Fa-f]+)\s*,\s*0x([0-9A-Fa-f]+)\s*,\s*"([^"]+)"\s*\)/g;
  const out = [];
  let m;
  while ((m = re.exec(block)) !== null) {
    out.push([parseInt(m[1], 16), parseInt(m[2], 16), m[3]]);
  }
  if (out.length === 0) fail("BCM_CANDIDATES parsed empty");
  return out;
}

function parseBcmAlgorithms(src) {
  const block = sliceBlock(src, "BCM_ALGORITHMS");
  // Each tuple: ('Name', <fn-ref-or-lambda>, "Description"),
  // The middle field can be a bare identifier or a `lambda s: ...` expression
  // that may contain commas, so match name + final quoted description per tuple.
  const lineRe = /\(\s*'([^']+)'\s*,[\s\S]*?,\s*"([^"]+)"\s*\)/g;
  const out = [];
  let m;
  while ((m = lineRe.exec(block)) !== null) {
    out.push([m[1], m[2]]);
  }
  if (out.length === 0) fail("BCM_ALGORITHMS parsed empty");
  return out;
}

function buildOutput({ modules, bcmCandidates, algos }) {
  const header = [
    "// AUTO-GENERATED by scripts/generate-quickref-data.mjs",
    "// Source of truth: public/srt_lab.py",
    "// Do not edit by hand. Run `pnpm --filter @workspace/srt-lab build` or",
    "// `node scripts/generate-quickref-data.mjs` to regenerate.",
    "",
  ].join("\n");

  const modulesOut =
    "export const QR_MODULES = [\n" +
    modules
      .map(
        (m) =>
          `  { code: ${JSON.stringify(m.code)}, tx: 0x${m.tx
            .toString(16)
            .toUpperCase()
            .padStart(3, "0")}, rx: 0x${m.rx
            .toString(16)
            .toUpperCase()
            .padStart(3, "0")}, desc: ${JSON.stringify(m.desc)} },`,
      )
      .join("\n") +
    "\n];\n";

  const candOut =
    "export const QR_BCM_CANDIDATES = [\n" +
    bcmCandidates
      .map(
        ([tx, rx, d]) =>
          `  [0x${tx.toString(16).toUpperCase().padStart(3, "0")}, 0x${rx
            .toString(16)
            .toUpperCase()
            .padStart(3, "0")}, ${JSON.stringify(d)}],`,
      )
      .join("\n") +
    "\n];\n";

  const algosOut =
    "export const QR_ALGOS = [\n" +
    algos.map(([n, d]) => `  [${JSON.stringify(n)}, ${JSON.stringify(d)}],`).join("\n") +
    "\n];\n";

  return [header, modulesOut, candOut, algosOut].join("\n");
}

function main() {
  const check = process.argv.includes("--check");
  if (!existsSync(PY_PATH)) fail(`source not found: ${PY_PATH}`);
  const src = readFileSync(PY_PATH, "utf8");
  const modules = parseModules(src);
  const bcmCandidates = parseBcmCandidates(src);
  const algos = parseBcmAlgorithms(src);
  const next = buildOutput({ modules, bcmCandidates, algos });

  if (check) {
    const cur = existsSync(OUT_PATH) ? readFileSync(OUT_PATH, "utf8") : "";
    if (cur !== next) {
      console.error(
        "generate-quickref-data: src/lib/quickRefData.generated.js is out of sync with public/srt_lab.py.",
      );
      console.error("Run: pnpm --filter @workspace/srt-lab quickref:gen");
      process.exit(1);
    }
    console.log("quickref data in sync");
    return;
  }

  writeFileSync(OUT_PATH, next);
  console.log(
    `generate-quickref-data: wrote ${OUT_PATH} (modules=${modules.length}, bcm=${bcmCandidates.length}, algos=${algos.length})`,
  );
}

main();
