#!/usr/bin/env node
/**
 * extract-alfaobd-algorithms.mjs
 *
 * Codegen for the AlfaOBD seed-key catalog. Reads the
 * `alfaobd_algorithm_catalog.json` drop (380 w6 wrappers + 360 w7
 * wrappers + dispatcher map) shipped under `attached_assets/` and
 * emits `src/lib/alfaobdAlgorithms.generated.js` exporting:
 *
 *   AOBD_W6                { name: [r, s] }     — 380 entries, fully decoded
 *   AOBD_W7_UNVERIFIED     { name: [n, o, p] }  — 360 entries, DATA ONLY
 *                                                  (cipher core not yet ported;
 *                                                  the "w7 ≡ w6 with p=0xFFFFFFFF"
 *                                                  hypothesis was tested and
 *                                                  rejected — see test file)
 *   AOBD_W7                — back-compat alias pointing at AOBD_W7_UNVERIFIED
 *   AOBD_DISPATCH          { family|ecu: { level: wrapperName } }
 *
 * The catalog JSON is the source of truth; do not hand-copy values
 * into source. Re-run after dropping a corrected catalog file in
 * `attached_assets/`.
 *
 * Usage:
 *   node scripts/extract-alfaobd-algorithms.mjs           # write
 *   node scripts/extract-alfaobd-algorithms.mjs --check   # CI parity
 */
import { readFileSync, writeFileSync, existsSync, readdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const REPO_ROOT = resolve(ROOT, "../..");
const ASSETS = resolve(REPO_ROOT, "attached_assets");
const OUT_PATH = resolve(ROOT, "src/lib/alfaobdAlgorithms.generated.js");

function findCatalog() {
  const matches = readdirSync(ASSETS)
    .filter((f) => /^alfaobd_algorithm_catalog.*\.json$/.test(f))
    .sort();
  if (!matches.length) {
    throw new Error(
      "alfaobd_algorithm_catalog*.json not found in attached_assets/"
    );
  }
  return resolve(ASSETS, matches[matches.length - 1]);
}

function asU32Hex(v) {
  // Accept "0x...", decimal-as-string, or number; emit 0xXXXXXXXX.
  let n;
  if (typeof v === "number") n = v >>> 0;
  else if (typeof v === "string") {
    n = (v.startsWith("0x") || v.startsWith("0X")) ? Number(v) : Number(v);
    n = n >>> 0;
  } else throw new Error("bad u32: " + v);
  return "0x" + n.toString(16).toUpperCase().padStart(8, "0");
}

function emit() {
  const catalogPath = findCatalog();
  const cat = JSON.parse(readFileSync(catalogPath, "utf8"));
  if (!cat.w6 || !cat.w7 || !cat.dispatch) {
    throw new Error("catalog JSON missing required keys");
  }

  const w6Names = Object.keys(cat.w6).sort();
  const w7Names = Object.keys(cat.w7).sort();

  const w6Lines = w6Names.map((n) => {
    const [r, s] = cat.w6[n];
    return `  ${JSON.stringify(n)}: [${asU32Hex(r)}, ${asU32Hex(s)}],`;
  });
  const w7Lines = w7Names.map((n) => {
    const [a, b, c] = cat.w7[n];
    return `  ${JSON.stringify(n)}: [${asU32Hex(a)}, ${asU32Hex(b)}, ${asU32Hex(c)}],`;
  });

  // Dispatch: pure projection of cat.dispatch — no hand augmentation
  // here. The "branch documented in README but wrapper not yet traced"
  // ECU placeholders live in src/lib/alfaobdDispatchAuxiliary.js as
  // hand-curated metadata, NOT inside this generated file. Consumers
  // who want the wider view import both modules and merge.
  const dispKeys = Object.keys(cat.dispatch).sort();
  const dispLines = dispKeys.map((k) => {
    const lvls = cat.dispatch[k];
    const lvlKeys = Object.keys(lvls).sort();
    const inner = lvlKeys
      .map((lk) => `    ${JSON.stringify(lk)}: ${JSON.stringify(lvls[lk])},`)
      .join("\n");
    return `  ${JSON.stringify(k)}: {\n${inner}\n  },`;
  });

  const meta = {
    w6_count: w6Names.length,
    w7_count: w7Names.length,
    dispatch_keys: dispKeys.length,
    source: catalogPath.split("/").pop(),
    note:
      "w6 = parameterized linear cipher (alfaW6 in algos.js); " +
      "w7 = big-integer arithmetic core, parameters staged but cipher not yet ported. " +
      "dispatch is a pure projection of the catalog JSON. README-listed " +
      "but-not-yet-traced ECU branches live in alfaobdDispatchAuxiliary.js.",
  };

  return `// AUTO-GENERATED by scripts/extract-alfaobd-algorithms.mjs
// Source: attached_assets/${meta.source}
// Do not edit by hand. Re-run \`pnpm --filter @workspace/srt-lab codegen:alfaobd-algos\`.
//
// w6: ${meta.w6_count} entries (fully decoded — see alfaW6 in algos.js)
// w7: ${meta.w7_count} entries (DATA ONLY — cipher core not yet translated)
// dispatch: ${meta.dispatch_keys} keys (8 ECU families + per-ECU branches)
//
// VERIFICATION STATUS — "w7 ≡ w6 with p=0xFFFFFFFF" hypothesis (task #424):
// The SRTLabJailbreakEdition_v2 paste asserted that the w7 cipher collapses
// to w6 when its third parameter p is forced to 0xFFFFFFFF, and shipped a
// 740-entry unified table claiming byte-identical output. We tested the
// claim and REJECTED it for two independent reasons:
//   1. Zero seed→key fixtures exist (see alfaobd_seedkey_README) — the
//      hypothesis is unfalsifiable from any captured ECU transaction.
//   2. The v2 paste's own data contradicts itself: of the 110 catalog w7
//      entries with p=0xFFFFFFFF, only 87 of the paste's (r,s) pairs
//      match the catalog's (n,o); 23 diverge. A clean substitution would
//      have produced 110/110 matches. See algos.alfaobd.test.mjs for the
//      full cross-check and named divergent samples.
// Outcome: all 360 w7 entries stay in AOBD_W7_UNVERIFIED. Adopting the
// v2 paste's table would silently ship 82 (23 + 59 from the p=0 subset)
// algorithm rows that disagree with our extraction. AOBD_W6 is unchanged.

export const AOBD_W6 = {
${w6Lines.join("\n")}
};

// 360 w7 wrappers staged with their per-ECU (n, o, p) parameters. The cipher
// core that consumes these is NOT translated; do not call alfaW6 with these
// values and do not migrate them into AOBD_W6 without first cross-checking
// against a real seed→key capture. See header comment for the full rationale.
export const AOBD_W7_UNVERIFIED = {
${w7Lines.join("\n")}
};

// Back-compat alias. Existing consumers (SeedTab, tests) imported this
// name before the rename; keep it pointing at the unverified table so
// the UI's advisory rendering keeps working unchanged.
export const AOBD_W7 = AOBD_W7_UNVERIFIED;

export const AOBD_DISPATCH = {
${dispLines.join("\n")}
};

export const AOBD_META = ${JSON.stringify(meta, null, 2)};
`;
}

function main() {
  const check = process.argv.includes("--check");
  let next;
  try {
    next = emit();
  } catch (e) {
    if (check) {
      console.log(
        `[extract-alfaobd-algorithms] skipping --check: ${e.message}`
      );
      return;
    }
    if (existsSync(OUT_PATH)) {
      console.warn(
        `[extract-alfaobd-algorithms] ${e.message}. Committed ${OUT_PATH} exists, leaving it unchanged.`
      );
      return;
    }
    console.error(`[extract-alfaobd-algorithms] ${e.message}`);
    process.exit(1);
  }
  if (check) {
    if (!existsSync(OUT_PATH)) {
      console.error(
        `[extract-alfaobd-algorithms] ${OUT_PATH} missing — run codegen.`
      );
      process.exit(1);
    }
    const cur = readFileSync(OUT_PATH, "utf8");
    if (cur !== next) {
      console.error(
        "[extract-alfaobd-algorithms] generated output is out of sync with catalog JSON. " +
          "Run `pnpm --filter @workspace/srt-lab codegen:alfaobd-algos`."
      );
      process.exit(1);
    }
    console.log("[extract-alfaobd-algorithms] in sync.");
    return;
  }
  writeFileSync(OUT_PATH, next);
  const sz = Buffer.byteLength(next, "utf8");
  console.log(
    `[extract-alfaobd-algorithms] wrote ${OUT_PATH} (${sz} bytes)`
  );
}

main();
