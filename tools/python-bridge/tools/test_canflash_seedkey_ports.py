def _resolve_fn(module):
    """Return the canflash_seedkey unlock function for a module name.

    Catalog uses the bare module name (matches the DLL basename); the Python
    function uses the ``unlock_<module>`` convention.  We try the convention
    first and fall back to a case-insensitive search so callers that pass
    'HB_ccn' vs 'hb_ccn' both work.
    """
    candidate = 'unlock_' + module
    fn = getattr(canflash_seedkey, candidate, None)
    if fn is not None:
        return fn
    target = candidate.lower()
    for name in dir(canflash_seedkey):
        if name.lower() == target:
            return getattr(canflash_seedkey, name)
    return None


def _normalize(actual, expected):
    """Compare allowing 16-bit ports to leave high bits clear.

    Some seed-key algorithms operate on 16-bit halves; the DLL leaves stale
    upper bits in EAX while the Python port returns a clean u16.  When the
    expected value fits in 16 bits we accept either form.
    """
    actual &= 0xFFFFFFFF
    expected &= 0xFFFFFFFF
    if (expected >> 16) == 0 and (actual & 0xFFFF) == expected:
        return True
    return actual == expected


class CanflashSeedkeyPinnedVectors(unittest.TestCase):
    """Each pinned (seed, key) pair is asserted against the live Python port."""

    def test_every_module_has_at_least_eight_vectors(self):
        short = {m: len(v) for m, v in VECTORS.items() if len(v) < 8}
        self.assertEqual(
            short, {},
            f'modules with <8 pinned vectors (regenerate via harvester): {short}',
        )

    def test_all_modules_resolve_to_a_callable(self):
        unresolved = []
        for module in VECTORS:
            if _resolve_fn(module) is None:
                unresolved.append(module)
        self.assertEqual(
            unresolved, [],
            f'pinned modules without an importable unlock_<module>: {unresolved}',
        )

    def test_vectors_cover_every_eligible_catalog_port(self):
        """Guard against silent omission when new ports are added.

        The set of pinned modules must equal every catalog entry that
        (a) is marked ``reversed`` and (b) has its python_function defined
        in ``canflash_seedkey``.  If a new port is added without re-running
        the harvester, this test flips red and prints exactly which modules
        to add.
        """
        catalog_path = os.path.join(HERE, 'unlock_catalog.json')
        with open(catalog_path, 'r', encoding='utf-8') as f:
            catalog = json.load(f)
        eligible = set()
        for entry in catalog['entries']:
            if entry.get('status') != 'reversed':
                continue
            pyfn = entry.get('python_function')
            if not pyfn or not hasattr(canflash_seedkey, pyfn):
                continue
            eligible.add(entry['module'])
        pinned = set(VECTORS)
        missing = eligible - pinned
        extra = pinned - eligible
        self.assertEqual(
            (missing, extra), (set(), set()),
            f'\nVECTORS drifted from the catalog.\n'
            f'  Missing (need vectors): {sorted(missing)}\n'
            f'  Extra   (no catalog entry): {sorted(extra)}\n'
            f'Re-run _canflash_validate/harvest_seedkey_vectors.py.',
        )

    def test_vectors_match_python_ports(self):
        """The core regression guard: every pinned vector must reproduce.

        This is the test that flips red when a port drifts from the original
        DLL output.  Failures list the (module, seed) tuples so the
        responsible commit is easy to identify.
        """
        failures = []
        total = 0
        for module, pairs in VECTORS.items():
            fn = _resolve_fn(module)
            self.assertIsNotNone(fn, f'no callable for {module}')
            arity = fn.__code__.co_argcount
            for seed, expected in pairs.items():
                total += 1
                try:
                    got = fn(seed, 0) if arity >= 2 else fn(seed)
                except Exception as exc:  # noqa: BLE001
                    failures.append((module, hex(seed), 'EXC', repr(exc)))
                    continue
                if not _normalize(got, expected):
                    failures.append(
                        (module, hex(seed), hex(got & 0xFFFFFFFF), hex(expected)),
                    )
        self.assertEqual(
            failures, [],
            f'\n{len(failures)} of {total} pinned vectors regressed:\n'
            + '\n'.join(f'  {f}' for f in failures[:20])
            + ('' if len(failures) <= 20 else f'\n  ... and {len(failures) - 20} more'),
        )


